
public class Pattern3 {
    
    public static void main(String []args){
        int n = 0;
        for(int row = 1;row<=n;row++){
            for(int col=1;col<=n;col++){
                System.out.print(" ");
                {
                    for( col=1;col<=2*row-1;col++){
                        System.out.print("*");
                        
                    }
                    
                }
                System.out.println(" ");
                
            }
            
            
        }
        
    
}
}
